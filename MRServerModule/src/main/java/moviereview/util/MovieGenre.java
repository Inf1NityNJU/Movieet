public enum MovieGenre {
    ALL,
    ACTION,
    ADVENTURE,
    ANIMATION, //动画片
    ANIME, //日本动画
    ART,
    BOXED_SETS, //BOXED系列
    CLASSICS,
    COMEDY,
    DOCUMENTARY,
    DRAMA,
    EDUCATIONAL,
    EXERCISE,
    FITNESS,
    FAITH,
    SPIRITUALITY,
    FANTASY,
    HOLIDAY,
    SEASONAL,
    HORROR,
    INDIE, //非主流或非商业目的的电影
    KIDS,
    FAMILY,
    MILITARY,
    WAR,
    MUSIC,
    CONCERTS,
    MYSTERY,
    THRILLERS,
    ROMANCE,
    SCIENCE,
    FICTION;

    public String getGenreName() {
        return toString().charAt(0) + toString().toLowerCase().substring(1);
    }
}
